SELECT CUSTOMER_ID, SUM(AMOUNT) AS TOTAL_COST_OF_PURCHASES FROM INVOICE GROUP BY CUSTOMER_ID;
SELECT * FROM INVOICE WHERE EXTRACT(MONTH FROM SYSDATE)=EXTRACT(MONTH FROM INVOICE_DATE) AND EXTRACT(YEAR FROM SYSDATE)=EXTRACT(YEAR FROM INVOICE_DATE) ORDER BY INVOICE_DATE DESC;
SELECT CUSTOMER_ID, MAX(AMOUNT) AS MAX_PURCHASE FROM INVOICE WHERE ROWNUM<=3 GROUP BY CUSTOMER_ID ORDER BY MAX_PURCHASE DESC;

